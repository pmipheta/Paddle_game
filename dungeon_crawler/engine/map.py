class Map:
    def __init__(self):
        
        self.map = [
            list("########################"),
            list("#..........#...........#"),
            list("#..........#...........#"),
            list("#..........#...........#"),
            list("#..........#####.......#"),
            list("#......................#"),
            list("#....######............#"),
            list("#......................#"),
            list("#.............####.....#"),
            list("#......................#"),
            list("#..........#...........#"),
            list("#..........#...........#"),
            list("#..........#...........#"),
            list("#..........#####.......#"),
            list("#......................#"),
            list("#....######............#"),
            list("#......................#"),
            list("########################"),
        ]
    
    def can_move(self,row,col) :
        if row < 0 or row >= len(self.map):
            return False
        if col <0 or col >= len(self.map[0]):
            return False
        
        if self.map[row][col] == "#":
            return False
        
        return self.map[row][col] != "#"
 
